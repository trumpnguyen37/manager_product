    private void Showdatabase(String idName) throws SQLException{
        try {
            tbproduct.removeAll();
            String[] arr={"Mã SP","Tên SP","Số Lượng","Giá Tiền"};
            DefaultTableModel model = new DefaultTableModel(arr,0);
            Connection connect = JDBCConnection();

            String sql ="select * from Product where idName = ?";

            PreparedStatement pstmt= connect.prepareStatement(sql);
            pstmt.setString(1, SignIn.idName);
            ResultSet rs =   pstmt.executeQuery();
            
            while(rs.next()){
                Vector vt =new Vector();
                vt.add(rs.getString("idProduct"));
                vt.add(rs.getString("tenSP"));
                vt.add(rs.getInt("soLuong"));
                vt.add(rs.getDouble("giaTien"));
                model.addRow(vt);

            }
            tbproduct.setModel(model);
        } catch (Exception e) {
        }
      }
