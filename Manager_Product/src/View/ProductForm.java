    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        StringBuilder sb= new StringBuilder();
       if(txtProductID.getText().equals("")){
           sb.append("Mã Product không được dể trống ");
           txtProductID.setBackground(Color.red);
       }else{
           txtProductID.setBackground(Color.white);
       }
       if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
       }
       if (JOptionPane.showConfirmDialog(this, "bạn có muốn xóa tài liệu không")== JOptionPane.NO_OPTION){
           return;
       }
        try {
            ProductDao dao = new ProductDao() ; 
            
           dao.delete(txtProductID.getText());
            JOptionPane.showMessageDialog(this, "Product được xoá");
            
            Showdatabase(SignIn.idName);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Errol " +e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

