    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        StringBuilder sb= new StringBuilder();
       if(txtProductID.getText().equals("")){
           sb.append("Mã Tài Liệu không được dể trống");
           txtProductID.setBackground(Color.red);
       }else{
           txtProductID.setBackground(Color.white);
       }
       if(sb.length()>0){
           JOptionPane.showMessageDialog(this, sb);
           return;
       }
        try {
            
            ProductService pro = new ProductService() ; 
            pro.setIdName(txtidName.getText());
            pro.setIdProduct(txtProductID.getText());
            pro.setTenSP(txtTenSP.getText());
            pro.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
            pro.setGiatien(Double.parseDouble(txtGiaTien.getText()));
           
           
            ProductDao prodao = new ProductDao();
            prodao.insertProduct(pro);
            
            JOptionPane.showMessageDialog(this, "tài liêu đã đc lưu");
            
            Showdatabase(SignIn.idName);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Errol " +e.getMessage());
            e.printStackTrace();
            
            
           
        }
    
    }//GEN-LAST:event_btnAddActionPerformed

